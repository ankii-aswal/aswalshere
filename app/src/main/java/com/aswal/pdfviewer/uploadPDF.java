package com.aswal.pdfviewer;

public class uploadPDF {
    public String name;
    public String url;

    public uploadPDF(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public uploadPDF() {
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }


}



